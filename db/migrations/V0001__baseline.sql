  CREATE OR REPLACE FORCE EDITIONABLE VIEW "WKSP_ESGAPEX"."ESG_INDEX_SECTOR_DAILY" ("INDEX_CODE", "D", "SECTOR", "SECTOR_RET", "SECTOR_WEIGHT") DEFAULT COLLATION "USING_NLS_COMP"  AS 
  WITH w as (
  SELECT index_code, ticker, trade_date, weight,
         LAST_VALUE(weight IGNORE NULLS) OVER (PARTITION BY index_code, ticker ORDER BY trade_date
           ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) AS w_eff
  FROM ESG_CONSTITUENT_PRICES
),
r as (
  SELECT index_code, ticker, trade_date, close_adj,
         LAG(close_adj) OVER (PARTITION BY index_code, ticker ORDER BY trade_date) AS prev_close
  FROM ESG_CONSTITUENT_PRICES
)
SELECT c.index_code, c.trade_date AS d, comp.sector,
       AVG((r.close_adj/r.prev_close)-1) KEEP (DENSE_RANK LAST ORDER BY r.trade_date) AS sector_ret,
       SUM(w.w_eff) / NULLIF(SUM(SUM(w.w_eff)) OVER (PARTITION BY c.index_code, c.trade_date),0) AS sector_weight
FROM ESG_CONSTITUENT_PRICES c
JOIN r ON r.index_code=c.index_code AND r.ticker=c.ticker AND r.trade_date=c.trade_date
LEFT JOIN w ON w.index_code=c.index_code AND w.ticker=c.ticker AND w.trade_date=c.trade_date
LEFT JOIN ESG_COMPANIES comp ON comp.ticker=c.ticker
GROUP BY c.index_code, c.trade_date, comp.sector
;
