  CREATE OR REPLACE EDITIONABLE PACKAGE BODY "WKSP_ESGAPEX"."SC_HTTP_PROXY" as
  function err_json(p_where varchar2) return clob is
  begin
    return
      '{"type":"error","where":' || apex_escape.json(p_where) ||
      ',"stack":'      || apex_escape.json(dbms_utility.format_error_stack) ||
      ',"backtrace":'  || apex_escape.json(dbms_utility.format_error_backtrace) ||
      ',"callstack":'  || apex_escape.json(dbms_utility.format_call_stack) || '}';
  end;

  function get(p_url varchar2) return clob is
    req  utl_http.req;
    res  utl_http.resp;
    buf  varchar2(32767);
    outc clob;
  begin
    utl_http.set_transfer_timeout(30);
    req := utl_http.begin_request(p_url, 'GET', 'HTTP/1.1');
    utl_http.set_header(req, 'Accept', 'application/json');
    res := utl_http.get_response(req);

    loop
      begin
        utl_http.read_text(res, buf, 32767);
        outc := outc || buf;
      exception when utl_http.end_of_body then exit; end;
    end loop;

    utl_http.end_response(res);
    return outc;
  exception
    when others then
      return err_json('utl_http');
  end;

  function tcp_probe(p_host varchar2, p_port number) return clob is
    c utl_tcp.connection;
  begin
    c := utl_tcp.open_connection(remote_host => p_host, remote_port => p_port, tx_timeout => 5);
    utl_tcp.close_connection(c);
    return '{"ok":true}';
  exception
    when others then
      return err_json('utl_tcp');
  end;
end sc_http_proxy;

