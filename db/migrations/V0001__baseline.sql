--------------------------------------------------------
--  DDL for Procedure ESG_CALC_ATTRIBUTION
--------------------------------------------------------
set define off;

  CREATE OR REPLACE EDITIONABLE PROCEDURE "WKSP_ESGAPEX"."ESG_CALC_ATTRIBUTION" (
  p_index_code IN VARCHAR2,
  p_bench_code IN VARCHAR2,
  p_start_date IN DATE,
  p_end_date   IN DATE
) AS
BEGIN
  MERGE INTO ESG_ATTRIBUTION_DAILY t
  USING (
    SELECT i.d AS calc_date, i.index_code, b.code AS bench_code, i.sector,
           i.sector_weight AS weight_idx, i.sector_ret AS ret_idx,
           b.sector_weight_bench AS weight_bench, b.sector_ret_bench AS ret_bench,
           (i.sector_weight - b.sector_weight_bench) * b.sector_ret_bench AS alloc_effect,
           b.sector_weight_bench * (i.sector_ret - b.sector_ret_bench)    AS select_effect,
           (i.sector_weight - b.sector_weight_bench) * (i.sector_ret - b.sector_ret_bench) AS interaction,
           (SELECT ret FROM ESG_INDEX_DAILY_RET WHERE index_code=i.index_code AND d=i.d) -
           (SELECT ret FROM ESG_BENCH_DAILY_RET WHERE code=b.code AND d=b.d) AS active_return
    FROM ESG_INDEX_SECTOR_DAILY i
    JOIN ESG_BENCH_SECTOR_DAILY b ON b.d = i.d
    WHERE i.index_code = p_index_code AND b.code = p_bench_code
      AND i.d BETWEEN p_start_date AND p_end_date
  ) s
  ON (t.calc_date=s.calc_date AND t.index_code=s.index_code AND t.bench_code=s.bench_code AND NVL(t.sector,'~')=NVL(s.sector,'~'))
  WHEN MATCHED THEN UPDATE SET
    t.weight_idx=s.weight_idx, t.ret_idx=s.ret_idx, t.weight_bench=s.weight_bench, t.ret_bench=s.ret_bench,
    t.alloc_effect=s.alloc_effect, t.select_effect=s.select_effect, t.interaction=s.interaction, t.active_return=s.active_return
  WHEN NOT MATCHED THEN INSERT (calc_date,index_code,bench_code,sector,weight_idx,ret_idx,weight_bench,ret_bench,alloc_effect,select_effect,interaction,active_return)
  VALUES (s.calc_date,s.index_code,s.bench_code,s.sector,s.weight_idx,s.ret_idx,s.weight_bench,s.ret_bench,s.alloc_effect,s.select_effect,s.interaction,s.active_return);
END;

