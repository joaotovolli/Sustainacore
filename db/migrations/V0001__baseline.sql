  CREATE OR REPLACE EDITIONABLE PACKAGE BODY "WKSP_ESGAPEX"."PKG_NEWS_REPO" AS
  PROCEDURE upsert_item(
    p_item_table IN VARCHAR2,
    p_id         IN NUMBER,
    p_dt_pub     IN TIMESTAMP,
    p_ticker     IN VARCHAR2,
    p_title      IN VARCHAR2,
    p_url        IN VARCHAR2,
    p_source     IN VARCHAR2,
    p_summary    IN CLOB,
    p_text       IN CLOB
  ) IS
  BEGIN
    IF p_item_table = 'NEWS_ITEMS' THEN
      MERGE INTO NEWS_ITEMS t
      USING (SELECT p_id id FROM dual) s
      ON (t.id = s.id)
      WHEN MATCHED THEN UPDATE SET
        dt_pub  = p_dt_pub,
        ticker  = p_ticker,
        title   = p_title,
        url     = p_url,
        source  = p_source,
        summary = p_summary
      WHEN NOT MATCHED THEN
        INSERT (dt_pub, ticker, title, url, source, summary)
        VALUES (p_dt_pub, p_ticker, p_title, p_url, p_source, p_summary);
    ELSIF p_item_table = 'ESG_NEWS' THEN
      MERGE INTO ESG_NEWS t
      USING (SELECT p_id id FROM dual) s
      ON (t.id = s.id)
      WHEN MATCHED THEN UPDATE SET
        date_published = CAST(p_dt_pub AS DATE),
        title = p_title, url = p_url, source = p_source, text = p_text
      WHEN NOT MATCHED THEN
        INSERT (company, title, url, source, date_published, text)
        VALUES (p_ticker, p_title, p_url, p_source, CAST(p_dt_pub AS DATE), p_text);
    ELSE
      RAISE_APPLICATION_ERROR(-20001, 'Invalid p_item_table');
    END IF;
  END;

  PROCEDURE add_category(p_name IN VARCHAR2) IS
  BEGIN
    INSERT /*+ ignore_row_on_dupkey_index(NEWS_CATEGORIES(NAME)) */
      INTO NEWS_CATEGORIES(name) VALUES (TRIM(p_name));
  EXCEPTION WHEN DUP_VAL_ON_INDEX THEN NULL;
  END;

  PROCEDURE add_tag(p_name IN VARCHAR2) IS
  BEGIN
    INSERT /*+ ignore_row_on_dupkey_index(NEWS_TAGS(NAME)) */
      INTO NEWS_TAGS(name) VALUES (TRIM(p_name));
  EXCEPTION WHEN DUP_VAL_ON_INDEX THEN NULL;
  END;

  PROCEDURE link_category(p_item_table IN VARCHAR2, p_item_id IN NUMBER, p_category_name IN VARCHAR2) IS
    v_cat_id NUMBER;
  BEGIN
    SELECT category_id INTO v_cat_id FROM NEWS_CATEGORIES WHERE name = TRIM(p_category_name);
    INSERT INTO NEWS_ITEM_CATEGORIES(item_table, item_id, category_id)
    VALUES (p_item_table, p_item_id, v_cat_id);
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      add_category(p_category_name);
      link_category(p_item_table, p_item_id, p_category_name);
    WHEN DUP_VAL_ON_INDEX THEN NULL;
  END;

  PROCEDURE link_tag(p_item_table IN VARCHAR2, p_item_id IN NUMBER, p_tag_name IN VARCHAR2) IS
    v_tag_id NUMBER;
  BEGIN
    SELECT tag_id INTO v_tag_id FROM NEWS_TAGS WHERE name = TRIM(p_tag_name);
    INSERT INTO NEWS_ITEM_TAGS(item_table, item_id, tag_id)
    VALUES (p_item_table, p_item_id, v_tag_id);
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      add_tag(p_tag_name);
      link_tag(p_item_table, p_item_id, p_tag_name);
    WHEN DUP_VAL_ON_INDEX THEN NULL;
  END;

  PROCEDURE link_ticker(p_item_table IN VARCHAR2, p_item_id IN NUMBER, p_ticker IN VARCHAR2) IS
