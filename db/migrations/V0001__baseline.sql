          MIN(CASE WHEN TO_CHAR(trade_date,'YYYY')=TO_CHAR(SYSDATE,'YYYY') THEN close_adj END) - 1) AS bench_ytd
  FROM ESG_BENCHMARK_RETURNS
  WHERE TO_CHAR(trade_date,'YYYY')=TO_CHAR(SYSDATE,'YYYY')
  GROUP BY code
)
SELECT idx.index_code, bn.bench_code, idx.idx_ytd, bn.bench_ytd
FROM idx CROSS JOIN bn
