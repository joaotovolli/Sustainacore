  PROCEDURE calc_daily_levels(p_date IN DATE DEFAULT TRUNC(SYSDATE)) IS
    v_prev_val  index_levels.index_level_pr%TYPE;
    v_divisor   index_levels.divisor%TYPE;
  BEGIN
    SELECT index_level_pr, divisor
      INTO v_prev_val, v_divisor
      FROM index_levels
     WHERE dt = (SELECT MAX(dt) FROM index_levels)
       FOR UPDATE;

    -- Naive demo calc: sum equal-weight returns from attribution if available; else carry forward
    DECLARE
      v_ret NUMBER := NULL;
    BEGIN
      SELECT NVL(SUM(contrib_bps)/10000, 0) INTO v_ret
      FROM attribution WHERE dt = p_date;
      IF v_ret IS NULL THEN v_ret := 0; END IF;
      INSERT INTO index_levels(dt, index_level_pr, divisor, calc_version)
      VALUES (p_date, v_prev_val * (1 + v_ret), v_divisor, 'v1');
    END;
    COMMIT;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      -- Seed base per methodology: 2025-01-02 = 1000
      INSERT INTO index_levels(dt, index_level_pr, divisor, calc_version)
      VALUES (DATE '2025-01-02', 1000, 1, 'v1');
      COMMIT;
  END;

