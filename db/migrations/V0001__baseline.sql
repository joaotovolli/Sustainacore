  CREATE OR REPLACE FORCE EDITIONABLE VIEW "WKSP_ESGAPEX"."ESG_INDEX_VS_BENCH_YTD" ("INDEX_CODE", "BENCH_CODE", "IDX_YTD", "BENCH_YTD") DEFAULT COLLATION "USING_NLS_COMP"  AS 
  WITH idx AS (
  SELECT index_code,
         (MAX(CASE WHEN TO_CHAR(level_date,'YYYY')=TO_CHAR(SYSDATE,'YYYY') THEN index_level END) /
          MIN(CASE WHEN TO_CHAR(level_date,'YYYY')=TO_CHAR(SYSDATE,'YYYY') THEN index_level END) - 1) AS idx_ytd
  FROM ESG_INDEX_LEVELS
  WHERE TO_CHAR(level_date,'YYYY')=TO_CHAR(SYSDATE,'YYYY')
  GROUP BY index_code
),
bn AS (
  SELECT code AS bench_code,
